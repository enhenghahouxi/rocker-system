package com.alibaba.entity;

public class Participant {
	
	private int par_id;
	
	private String par_name;
	
	private String par_telephone;
	
	private String par_identityid;
	
	private int par_hasget;

	public int getPar_id() {
		return par_id;
	}

	public void setPar_id(int par_id) {
		this.par_id = par_id;
	}

	public String getPar_name() {
		return par_name;
	}

	public void setPar_name(String par_name) {
		this.par_name = par_name;
	}

	public String getPar_telephone() {
		return par_telephone;
	}

	public void setPar_telephone(String par_telephone) {
		this.par_telephone = par_telephone;
	}

	public String getPar_identityid() {
		return par_identityid;
	}

	public void setPar_identityid(String par_identityid) {
		this.par_identityid = par_identityid;
	}

	public int getPar_hasget() {
		return par_hasget;
	}

	public void setPar_hasget(int par_hasget) {
		this.par_hasget = par_hasget;
	}
	
}
