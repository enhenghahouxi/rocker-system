package com.alibaba.entity;


public class Pool {

	private int pool_id;
	
	private String pool_name;
	
	private String pool_telephone;
	
	private String pool_identityid;
	
	private int pool_hasget;

	public int getPool_id() {
		return pool_id;
	}

	public void setPool_id(int pool_id) {
		this.pool_id = pool_id;
	}

	public String getPool_name() {
		return pool_name;
	}

	public void setPool_name(String pool_name) {
		this.pool_name = pool_name;
	}

	public String getPool_telephone() {
		return pool_telephone;
	}

	public void setPool_telephone(String pool_telephone) {
		this.pool_telephone = pool_telephone;
	}

	public String getPool_identityid() {
		return pool_identityid;
	}

	public void setPool_identityid(String pool_identityid) {
		this.pool_identityid = pool_identityid;
	}

	public int getPool_hasget() {
		return pool_hasget;
	}

	public void setPool_hasget(int pool_hasget) {
		this.pool_hasget = pool_hasget;
	}
	
}
